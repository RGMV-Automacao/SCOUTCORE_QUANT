const namesLower = new Map(
  Object.entries(TEAM_NAME_MAP).map(([input, output]) => [input.toLowerCase(), output]),
);

function stripAccents(value) {
  return value.normalize('NFD').replace(/[\u0300-\u036f]/g, '');
}

export function resolveToOriginal(name) {
  if (!name) return name;
  const trimmed = String(name).trim();
  if (TEAM_NAME_MAP[trimmed]) return TEAM_NAME_MAP[trimmed];

  const lower = trimmed.toLowerCase();
  if (namesLower.has(lower)) return namesLower.get(lower);

  const stripped = stripAccents(lower);
  for (const [inputLower, output] of namesLower) {
    if (stripAccents(inputLower) === stripped) return output;
  }

  return trimmed;
}
